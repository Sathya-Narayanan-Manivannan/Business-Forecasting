urnsa = read.csv('UNRATENSA.csv')
unemp = ts(urnsa[,2],start = c(1948,1),frequency=12)
plot (unemp)
unemp_hold=ts(urnsa[,2],start=c(1948,1),end=c(2016,11),frequency=12)
plot(unemp_hold)
library(urca)
library(forecast)
ur_test=ur.df(unemp_hold,type='drift',lags=20,selectlags='AIC')
ur_test=ur.df(diff(unemp_hold),type='drift',lags=20,selectlags = 'AIC')
summary(ur_test)
plot(diff(unemp_hold))
acf(diff(unemp_hold),60)
pacf(diff(unemp_hold), 60)
rho=acf(diff(unemp_hold),36)
Arima(unemp_hold,order=c(1,1,1),seasonal=c(1,0,1))
Arima(unemp_hold,order=c(2,1,1),seasonal=c(1,0,1))
model=Arima(unemp_hold,order=c(1,1,1),seasonal=c(1,0,1))
acf(model$residuals)
Box.test(model$residuals,30,type='Ljung-Box')
unemp_hold_fore=forecast.Arima(model,h=6)
upper=ts(unemp_hold_fore$upper[,2],start=c(2016,12),frequency=12)
lower=ts(unemp_hold_fore$lower[,2],start=c(2016,12),frequency=12)
plot(cbind(window(unemp,start=c(2016,3),frequency=12),unemp_hold_fore$mean,upper,lower),plot.type="single",ylab='Forecast',lty=c("solid","solid","dashed","dashed"),col= c("blue","red","green","purple"))
